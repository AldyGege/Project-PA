const connection = require('../config/Database');

class Model_Mapel {

    static async getAll(){
        return new Promise((resolve, reject) => {
            connection.query('SELECT mapel.*, admin.nama_admin FROM mapel INNER JOIN admin ON mapel.id_admin = admin.id_admin ORDER BY mapel.id_mapel DESC', (err, rows) => {
                if(err){
                    reject(err);
                } else {
                    resolve(rows);
                }
            });
        });
    }

    static async Store(Data){
        return new Promise((resolve, reject) => {
            connection.query('insert into mapel set ?', Data, function(err, result){
                if(err){
                    reject(err);
                } else {
                    resolve(result);
                }
            })
        });
    }

    static async getId(id){
        return new Promise((resolve, reject) => {
            connection.query('select * from mapel where id_mapel = ' + id, (err,rows) => {
                if(err) {
                    reject(err);
                } else {
                    resolve(rows);
                }
            })
        })
    }
    static async Update(id, Data) {
        return new Promise((resolve, reject) => {
            connection.query('update mapel set ? where id_mapel =' + id, Data, function(err, result){
                if(err){
                    reject(err);
                    console.log(err);
                } else {
                    resolve(result);
                }
            })
        });
    }

    static async Delete(id) {
        return new Promise((resolve, reject) => {
            connection.query('delete from mapel where id_mapel =' + id, function(err,result){
                if(err) {
                    reject(err);
                } else {
                    resolve(result);
                }
            })
        });
    }

}


module.exports = Model_Mapel;